first_number = float(input("Enter your first number: "))
second_number = float(input("Enter your second number: "))

sum_result = first_number + second_number
difference_result = first_number - second_number
division_result = first_number / second_number
product_result = first_number * second_number

print(f"Sum of the numbers: {sum_result}")
print(f"Difference of the numbers: {difference_result}")
print(f"Division of the numbers: {division_result}")
print(f"Product of the numbers: {product_result}")